-- 최저 급여가 5000달러 이상인 직업이름을 검색하시오
SELECT JOB_ID, MIN_SALARY min_sal
FROM JOB_INFO
WHERE MIN_SALARY >= 5000;
-- 최저급여가 5000달러 이상인 직업 아이디를 검색한 뷰를 작성하시오
CREATE VIEW V_JOB_INFO 
AS SELECT JOB_ID, MIN_SALARY
    FROM JOB_INFO
    WHERE MIN_SALARY > 5000;
    
-- 최고급여와 최저급여의 차가 8000 이상인 자료를 검색하시오
SELECT *
FROM JOB_INFO
WHERE MAX_SALARY - MIN_SALARY >= 8000;
    
-- 직업 아이디의 이름이 AD로 시작하는 자료를 검색하시오
SELECT *
FROM JOB_INFO
WHERE JOB_ID LIKE 'AD%';

